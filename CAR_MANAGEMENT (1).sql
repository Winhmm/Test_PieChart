USE CarManagement

CREATE TABLE CUSTOMER(
	CUSTOMER_ID CHAR(9) CONSTRAINT CUSTOMER_CUSTOMERID_PK  PRIMARY KEY (CUSTOMER_ID),
	CUSTOMER_ADDRESS NVARCHAR(200),
	CUSTOMER_NAME NVARCHAR(100),
	CUSTOMER_NUMBERS CHAR(10),
);
CREATE TABLE EMPLOYEE(
	EMPLOYEE_ID CHAR(9) CONSTRAINT EMPLOYEE_EMPLOYEEID_PK PRIMARY KEY (EMPLOYEE_ID),
	EMPLOYEE_NUMBERS INT,
	EMPLOYEE_ACCOUNT VARCHAR(100),
	EMPLOYEE_PASSWORD varchar(100),
	EMPLOYEE_MANAGER_ID CHAR(9) CONSTRAINT EMPLOYEE_MANGER_FK FOREIGN KEY (EMPLOYEE_MANAGER_ID) REFERENCES EMPLOYEE(EMPLOYEE_ID),
	EMPLOYEE_ROLE NVARCHAR(50)
	
);

CREATE TABLE CAR (
	CAR_ID CHAR(9) CONSTRAINT CAR_CARID_PK PRIMARY KEY (CAR_ID),
	CAR_NAME NVARCHAR(100),
	CAR_BRAND NVARCHAR(100),
	CAR_TYPE CHAR(1),
	CAR_DESCRIBE NVARCHAR(300),
	CAR_PRICE DECIMAL(12,2),
	CAR_QUANTITY INT,
	CAR_IMAGE VARCHAR(100)
);

CREATE TABLE MANAGEMENT(
	MANAGEMENT_ID CHAR(9) CONSTRAINT MANAGEMENT_STT_PK PRIMARY KEY (MANAGEMENT_ID),
	CAR_ID CHAR(9) CONSTRAINT MANAGEMENT_CARID_FK FOREIGN KEY(CAR_ID) REFERENCES CAR(CAR_ID),
	EMPLOYEE_ID CHAR(9) CONSTRAINT MANAGEMENT_EMPLOYEEID_FK FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(EMPLOYEE_ID)
	
);
CREATE TABLE BUY(
	BUY_ID CHAR(9) CONSTRAINT BUY_BUYID_PK PRIMARY KEY (BUY_ID),
	CUSTOMER_ID CHAR(9) CONSTRAINT BUY_CUSTOMERID_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),
	CAR_ID CHAR(9) CONSTRAINT BUY_CARID_FK FOREIGN KEY (CAR_ID) REFERENCES CAR(CAR_ID),
	BUY_QUANTITY INT,
	BUY_DATE DATE
);

